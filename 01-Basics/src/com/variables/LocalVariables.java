package com.variables;

public class LocalVariables {
    public static void main(String[] args) {

        String localVariable = """
                -> Sometimes to meet temporary requirements, we can declare
                   a variable inside a method. Such variable is called local
                   variable and its scope lies only within the block.
                
                -> The only modifier applicable for local variable is final.
                """;

        System.out.println(localVariable);
    }
}
